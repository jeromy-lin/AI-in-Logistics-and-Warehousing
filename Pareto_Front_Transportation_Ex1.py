# Step 6：建立支配關係判斷函數
# ============================================================

def 是否支配(方案A, 方案B):

    A時間 = 方案A["配送完成時間（天）"]
    A成本 = 方案A["總配送成本（元）"]

    B時間 = 方案B["配送完成時間（天）"]
    B成本 = 方案B["總配送成本（元）"]

    不劣於B = (
        A時間 <= B時間
        and A成本 <= B成本
    )

    至少一項更好 = (
        A時間 < B時間
        or A成本 < B成本
    )

    return 不劣於B and 至少一項更好


