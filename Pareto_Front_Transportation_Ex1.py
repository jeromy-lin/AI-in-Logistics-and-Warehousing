# Step 3：建立配送完成時間函數
# ============================================================

def 計算配送完成時間(派遣車輛數, 每車每日加開趟次):

    # 每車每日總趟次＝基本趟次＋加開趟次
    每車每日總趟次 = (
        每車每日基本趟次
        + 每車每日加開趟次
    )

    # 每日配送能力＝車輛數×每趟容量×每車每日總趟次
    每日配送能力 = (
        派遣車輛數
        * 每車每趟容量
        * 每車每日總趟次
    )

    理貨時間天數 = (
        固定理貨時間
        / 每日正常工時
    )

    配送時間天數 = (
        待配送貨量
        / 每日配送能力
    )

    # 目標一：配送完成時間最小化
    配送完成時間天數 = (
        理貨時間天數
        + 配送時間天數
    )

    return 配送完成時間天數


# ============================================================
# Step 4：建立總配送成本函數
# ============================================================

def 計算總配送成本(
    派遣車輛數,
    每車每日加開趟次,
    配送完成時間天數
):

    每車每日總趟次 = (
        每車每日基本趟次
        + 每車每日加開趟次
    )

    貨物處理成本 = (
        待配送貨量
        * 每件貨物處理成本
    )

    車輛派遣成本 = (
        派遣車輛數
        * 每車派遣固定成本
    )

    車趟運行成本 = (
        派遣車輛數
        * 每車每日總趟次
        * 配送完成時間天數
        * 每趟運行成本
    )

    基本駕駛成本 = (
        派遣車輛數
        * 每日正常工時
        * 配送完成時間天數
        * 每位駕駛每小時工資
    )

    加開趟次駕駛成本 = (
        派遣車輛數
        * 每車每日加開趟次
        * 每趟平均作業時間
        * 配送完成時間天數
        * 每位駕駛每小時工資
        * 加開趟次工資倍率
    )

    # 目標二：總配送成本最小化
    總配送成本 = (
        貨物處理成本
        + 車輛派遣成本
        + 車趟運行成本
        + 基本駕駛成本
        + 加開趟次駕駛成本
    )

    return {
        "貨物處理成本": 貨物處理成本,
        "車輛派遣成本": 車輛派遣成本,
        "車趟運行成本": 車趟運行成本,
        "基本駕駛成本": 基本駕駛成本,
        "加開趟次駕駛成本": 加開趟次駕駛成本,
        "總配送成本": 總配送成本
    }


