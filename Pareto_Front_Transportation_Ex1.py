# Step 14：標準化Pareto方案的時間與成本
# ============================================================

最小時間 = Pareto方案[
    "配送完成時間（天）"
].min()

最大時間 = Pareto方案[
    "配送完成時間（天）"
].max()

最小成本 = Pareto方案[
    "總配送成本（元）"
].min()

最大成本 = Pareto方案[
    "總配送成本（元）"
].max()

if 最大時間 == 最小時間:

    Pareto方案["標準化時間"] = 0.0

else:

    Pareto方案["標準化時間"] = (
        Pareto方案["配送完成時間（天）"]
        - 最小時間
    ) / (
        最大時間
        - 最小時間
    )

if 最大成本 == 最小成本:

    Pareto方案["標準化成本"] = 0.0

else:

    Pareto方案["標準化成本"] = (
        Pareto方案["總配送成本（元）"]
        - 最小成本
    ) / (
        最大成本
        - 最小成本
    )

Pareto方案["理想點距離"] = np.sqrt(
    Pareto方案["標準化時間"] ** 2
    + Pareto方案["標準化成本"] ** 2
)


